$(document).ready(function(){
	$(".form-btn").click(function(){
		$("#id_category").val($(this).text());
		$(".form-btn").addClass("button-primary");
		$(".form-add").addClass("button-primary");
		$(this).removeClass("button-primary");
		$("#form-category").css("display","none");
			
	});
	$(".form-add").click(function(){
		$("#form-category").css("display","block");
		$(".form-btn").addClass("button-primary");
		$(".form-add").addClass("button-primary");
		$(this).removeClass("button-primary");
	});
	$(".button-rounded").click(function(){
		$("#category_alert").css("display","block");
	})
})